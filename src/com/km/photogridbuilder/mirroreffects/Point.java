package com.km.photogridbuilder.mirroreffects;


public class Point
{

    private int x;
    private int y;

    public Point()
    {
    }

    public int getX()
    {
        return x;
    }

    public int getY()
    {
        return y;
    }

    public void setX(int i)
    {
        x = i;
    }

    public void setY(int i)
    {
        y = i;
    }
}
