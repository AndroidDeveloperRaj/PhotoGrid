package com.km.photogridbuilder.mirroreffects;


public class FloatPoint
{

    private float x;
    private float y;

    public FloatPoint()
    {
    }

    public float getX()
    {
        return x;
    }

    public float getY()
    {
        return y;
    }

    public void setX(float f)
    {
        x = f;
    }

    public void setY(float f)
    {
        y = f;
    }
}
